// Package gen walks a parsed Python AST (from pyast) and emits a Go
// source file whose block structure mirrors the Python source. Every
// Python name stays a Go name; every Python control flow construct
// becomes its Go counterpart wrapping rt.Truthy / rt.Compare / etc.
package gen

import (
	"bytes"
	"fmt"
	"strings"

	"github.com/tamnd/gopygo/pyast"
)

// Compile takes a Module node and returns a gofmt-ready Go source
// string. pkg is the package name to put at the top (usually "main").
func Compile(module pyast.Node, pkg string) ([]byte, error) {
	g := &gen{pkg: pkg}
	if err := g.emitModule(module); err != nil {
		return nil, err
	}
	return g.finish(), nil
}

type gen struct {
	pkg        string
	header     bytes.Buffer // package, imports, loadName
	body       bytes.Buffer // functions + main
	scope      *scope
	funcs      []string          // ordered def names at module scope
	moduleVars map[string]bool   // names assigned at module level
	used       map[string]bool   // runtime symbols referenced; purely informational
	tmp        int
}

type scope struct {
	parent  *scope
	params  map[string]bool
	locals  map[string]bool // names assigned in this function (or module)
	isModule bool
}

func (g *gen) fresh(prefix string) string {
	g.tmp++
	return fmt.Sprintf("%s%d", prefix, g.tmp)
}

func (g *gen) errf(n pyast.Node, format string, args ...any) error {
	return fmt.Errorf("gopygo: line %d col %d: %s", n.Line(), n.Col(),
		fmt.Sprintf(format, args...))
}

func (g *gen) finish() []byte {
	var out bytes.Buffer
	fmt.Fprintf(&out, "// Code generated by gopygo. DO NOT EDIT.\n\n")
	fmt.Fprintf(&out, "package %s\n\n", g.pkg)
	fmt.Fprintf(&out, "import (\n")
	fmt.Fprintf(&out, "\t\"fmt\"\n")
	fmt.Fprintf(&out, "\t\"os\"\n\n")
	fmt.Fprintf(&out, "\trt \"github.com/tamnd/gopygo/runtime\"\n")
	fmt.Fprintf(&out, ")\n\n")
	fmt.Fprintf(&out, "var globals = map[string]rt.Value{}\n\n")
	fmt.Fprintf(&out, "func loadName(name string) rt.Value {\n")
	fmt.Fprintf(&out, "\tif v, ok := globals[name]; ok { return v }\n")
	fmt.Fprintf(&out, "\tif v, ok := rt.Builtins[name]; ok { return v }\n")
	fmt.Fprintf(&out, "\tpanic(fmt.Sprintf(\"NameError: name %%q is not defined\", name))\n")
	fmt.Fprintf(&out, "}\n\n")
	out.Write(g.body.Bytes())
	return out.Bytes()
}

// emitModule lowers a whole Module node. It emits one Go func per
// top-level Python def, plus a main() that runs the module-level
// statements in order.
func (g *gen) emitModule(mod pyast.Node) error {
	if mod.Type() != "Module" {
		return fmt.Errorf("gopygo: expected Module at root, got %s", mod.Type())
	}
	body := mod.Children("body")

	// First pass: collect module-level assigned names.
	modScope := &scope{locals: map[string]bool{}, params: map[string]bool{}, isModule: true}
	collectAssigns(body, modScope.locals)
	g.moduleVars = modScope.locals

	// Emit nested defs first so main() can reference them.
	for _, n := range body {
		if n.Type() == "FunctionDef" {
			g.funcs = append(g.funcs, n.Str("name"))
			if err := g.emitFunctionDef(n); err != nil {
				return err
			}
		}
	}

	// Emit main().
	fmt.Fprintf(&g.body, "func main() {\n")
	fmt.Fprintf(&g.body, "\tdefer func() {\n")
	fmt.Fprintf(&g.body, "\t\tif r := recover(); r != nil {\n")
	fmt.Fprintf(&g.body, "\t\t\tfmt.Fprintln(os.Stderr, r)\n")
	fmt.Fprintf(&g.body, "\t\t\tos.Exit(1)\n")
	fmt.Fprintf(&g.body, "\t\t}\n")
	fmt.Fprintf(&g.body, "\t}()\n")

	// var decls for every module-local name, including functions.
	names := sortedKeys(modScope.locals)
	for _, name := range names {
		fmt.Fprintf(&g.body, "\tvar %s rt.Value\n", name)
	}
	// Bind top-level func names to their *rt.Func wrappers.
	for _, name := range g.funcs {
		fmt.Fprintf(&g.body, "\t%s = _fn_%s\n", name, name)
		fmt.Fprintf(&g.body, "\tglobals[%q] = %s\n", name, name)
	}
	// If the top-level has a print-like name bound elsewhere, fine.
	_ = names

	g.scope = modScope
	for _, n := range body {
		if n.Type() == "FunctionDef" {
			continue // already emitted
		}
		if err := g.emitStmt(n, "\t"); err != nil {
			return err
		}
	}
	fmt.Fprintf(&g.body, "}\n")
	return nil
}

// emitFunctionDef lowers one `def`.
func (g *gen) emitFunctionDef(n pyast.Node) error {
	name := n.Str("name")
	args := n.Child("args")
	if args == nil {
		return g.errf(n, "FunctionDef without args")
	}
	params, err := extractParams(args)
	if err != nil {
		return err
	}
	paramNames := make([]string, len(params))
	for i, p := range params {
		paramNames[i] = p
	}
	body := n.Children("body")

	sc := &scope{
		parent: nil,
		params: map[string]bool{},
		locals: map[string]bool{},
	}
	for _, p := range paramNames {
		sc.params[p] = true
	}
	collectAssigns(body, sc.locals)
	// Params are not redeclared as locals.
	for p := range sc.params {
		delete(sc.locals, p)
	}

	// Emit the wrapper *rt.Func constructor and the Go func body.
	fmt.Fprintf(&g.body, "var _fn_%s = &rt.Func{Name: %q, Arity: %d, Impl: _impl_%s}\n\n",
		name, name, len(paramNames), name)
	fmt.Fprintf(&g.body, "func _impl_%s(_args []rt.Value) (rt.Value, error) {\n", name)
	fmt.Fprintf(&g.body, "\tif len(_args) != %d { return nil, rt.TypeError(\"%s() takes %d positional arguments but %%d were given\", len(_args)) }\n",
		len(paramNames), name, len(paramNames))
	for i, p := range paramNames {
		fmt.Fprintf(&g.body, "\t%s := _args[%d]\n", p, i)
	}
	for _, l := range sortedKeys(sc.locals) {
		fmt.Fprintf(&g.body, "\tvar %s rt.Value\n", l)
	}
	fmt.Fprintf(&g.body, "\t_ = _args\n")

	prev := g.scope
	g.scope = sc
	defer func() { g.scope = prev }()

	for _, s := range body {
		if err := g.emitStmt(s, "\t"); err != nil {
			return err
		}
	}
	fmt.Fprintf(&g.body, "\treturn rt.None, nil\n")
	fmt.Fprintf(&g.body, "}\n\n")
	return nil
}

func extractParams(args pyast.Node) ([]string, error) {
	var out []string
	for _, a := range args.Children("args") {
		out = append(out, a.Str("arg"))
	}
	if len(args.Children("posonlyargs")) > 0 {
		return nil, fmt.Errorf("gopygo: positional-only args not supported")
	}
	if len(args.Children("kwonlyargs")) > 0 {
		return nil, fmt.Errorf("gopygo: keyword-only args not supported")
	}
	if args.Child("vararg") != nil {
		return nil, fmt.Errorf("gopygo: *args not supported")
	}
	if args.Child("kwarg") != nil {
		return nil, fmt.Errorf("gopygo: **kwargs not supported")
	}
	if len(args.Children("defaults")) > 0 {
		return nil, fmt.Errorf("gopygo: default args not supported")
	}
	return out, nil
}

// collectAssigns walks stmts (non-recursively across nested defs)
// and records every name that is an assignment target.
func collectAssigns(stmts []pyast.Node, out map[string]bool) {
	for _, s := range stmts {
		switch s.Type() {
		case "Assign":
			for _, t := range s.Children("targets") {
				collectTarget(t, out)
			}
		case "AugAssign", "AnnAssign":
			collectTarget(s.Child("target"), out)
		case "For":
			collectTarget(s.Child("target"), out)
			collectAssigns(s.Children("body"), out)
			collectAssigns(s.Children("orelse"), out)
		case "While", "If":
			collectAssigns(s.Children("body"), out)
			collectAssigns(s.Children("orelse"), out)
		case "FunctionDef":
			out[s.Str("name")] = true
			// Do not recurse; nested defs have their own scope.
		}
	}
}

func collectTarget(t pyast.Node, out map[string]bool) {
	if t == nil {
		return
	}
	switch t.Type() {
	case "Name":
		out[t.Str("id")] = true
	case "Tuple", "List":
		for _, e := range t.Children("elts") {
			collectTarget(e, out)
		}
	case "Subscript", "Attribute":
		// Not a new binding.
	}
}

// sortedKeys returns a deterministic sorted slice of map keys.
func sortedKeys(m map[string]bool) []string {
	out := make([]string, 0, len(m))
	for k := range m {
		out = append(out, k)
	}
	// simple insertion sort; we never have huge sets here
	for i := 1; i < len(out); i++ {
		for j := i; j > 0 && out[j-1] > out[j]; j-- {
			out[j-1], out[j] = out[j], out[j-1]
		}
	}
	return out
}

// isLocal reports whether name is bound in the current function
// scope (param or local).
func (g *gen) isLocal(name string) bool {
	if g.scope == nil {
		return false
	}
	return g.scope.params[name] || g.scope.locals[name]
}

// nameExpr returns the Go expression for a Python Name reference.
func (g *gen) nameExpr(name string) string {
	if g.isLocal(name) {
		return name
	}
	return fmt.Sprintf("loadName(%q)", name)
}

// quote returns a Go double-quoted literal.
func quote(s string) string {
	var b strings.Builder
	b.WriteByte('"')
	for _, r := range s {
		switch r {
		case '\\':
			b.WriteString(`\\`)
		case '"':
			b.WriteString(`\"`)
		case '\n':
			b.WriteString(`\n`)
		case '\r':
			b.WriteString(`\r`)
		case '\t':
			b.WriteString(`\t`)
		default:
			if r < 0x20 {
				fmt.Fprintf(&b, `\x%02x`, r)
			} else {
				b.WriteRune(r)
			}
		}
	}
	b.WriteByte('"')
	return b.String()
}
