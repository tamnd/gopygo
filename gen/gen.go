// Package gen walks a parsed Python AST and emits concrete-typed Go
// source. Inference and emission are fused into one pass: each
// expression emitter returns the Go code fragment plus the inferred
// type, and each statement emitter writes code directly while
// updating scope bindings.
//
// The emitted Go program imports only the Go standard library
// (fmt, strconv, ...) — never a gopygo package. Any construct the
// type system cannot resolve is a transpile-time error at the
// Python source location. Silent `any` fallback is not a feature.
package gen

import (
	"bytes"
	"fmt"

	"github.com/tamnd/gopygo/pyast"
	"github.com/tamnd/gopygo/types"
)

// Compile walks a Module node and returns a formatted Go source file.
func Compile(module pyast.Node, pkg string) ([]byte, error) {
	g := &gen{pkg: pkg, imports: map[string]bool{}}
	if err := g.emitModule(module); err != nil {
		return nil, err
	}
	return g.assemble(), nil
}

type gen struct {
	pkg     string
	out     bytes.Buffer // body: top-level vars, funcs, main
	imports map[string]bool
	scope   *scope
	tmp     int
	// returnType is the declared return type of the function currently
	// being emitted. Return statements check against it.
	returnType types.Type
	// helpers set by visitor mark which program-local helper funcs
	// should be emitted once at the bottom of the file.
	needAbsInt   bool
	needMinMaxI  bool
	needAtoi     bool
	needPyPrint  bool
}

type scope struct {
	parent *scope
	names  map[string]types.Type
}

func newScope(parent *scope) *scope {
	return &scope{parent: parent, names: map[string]types.Type{}}
}

func (s *scope) lookup(name string) (types.Type, bool) {
	for cur := s; cur != nil; cur = cur.parent {
		if t, ok := cur.names[name]; ok {
			return t, true
		}
	}
	return nil, false
}

func (g *gen) fresh(prefix string) string {
	g.tmp++
	return fmt.Sprintf("%s%d", prefix, g.tmp)
}

func (g *gen) need(pkg string) { g.imports[pkg] = true }

func (g *gen) errf(n pyast.Node, format string, args ...any) error {
	return fmt.Errorf("gopygo: line %d col %d: %s",
		n.Line(), n.Col(), fmt.Sprintf(format, args...))
}

func (g *gen) assemble() []byte {
	var hdr bytes.Buffer
	fmt.Fprintf(&hdr, "// Code generated by gopygo. DO NOT EDIT.\n\n")
	fmt.Fprintf(&hdr, "package %s\n\n", g.pkg)
	if len(g.imports) > 0 {
		fmt.Fprintf(&hdr, "import (\n")
		for _, p := range sortedImports(g.imports) {
			fmt.Fprintf(&hdr, "\t%q\n", p)
		}
		fmt.Fprintf(&hdr, ")\n\n")
	}
	hdr.Write(g.out.Bytes())
	g.emitHelpers(&hdr)
	return hdr.Bytes()
}

func (g *gen) emitHelpers(out *bytes.Buffer) {
	if g.needAbsInt {
		fmt.Fprintln(out, `
func absInt(x int64) int64 {
	if x < 0 {
		return -x
	}
	return x
}`)
	}
	if g.needMinMaxI {
		fmt.Fprintln(out, `
func minInt(xs ...int64) int64 {
	m := xs[0]
	for _, v := range xs[1:] {
		if v < m {
			m = v
		}
	}
	return m
}

func maxInt(xs ...int64) int64 {
	m := xs[0]
	for _, v := range xs[1:] {
		if v > m {
			m = v
		}
	}
	return m
}`)
	}
	if g.needAtoi {
		fmt.Fprintln(out, `
func mustAtoi64(s string) int64 {
	n, err := strconv.ParseInt(s, 10, 64)
	if err != nil {
		panic("invalid literal for int(): " + s)
	}
	return n
}`)
	}
	if g.needPyPrint {
		fmt.Fprintln(out, `
func pyRepr(v any) string {
	switch x := v.(type) {
	case bool:
		if x {
			return "True"
		}
		return "False"
	case float64:
		if x == float64(int64(x)) {
			return strconv.FormatFloat(x, 'f', 1, 64)
		}
		return strconv.FormatFloat(x, 'g', -1, 64)
	case string:
		return x
	}
	return fmt.Sprint(v)
}

func pyPrintln(args ...any) {
	parts := make([]string, len(args))
	for i, a := range args {
		parts[i] = pyRepr(a)
	}
	fmt.Println(strings.Join(parts, " "))
}`)
	}
}

func sortedImports(m map[string]bool) []string {
	out := make([]string, 0, len(m))
	for k := range m {
		out = append(out, k)
	}
	for i := 1; i < len(out); i++ {
		for j := i; j > 0 && out[j-1] > out[j]; j-- {
			out[j-1], out[j] = out[j], out[j-1]
		}
	}
	return out
}
